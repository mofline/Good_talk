function copy_1() {
    //要素の文字列を取得
    var text = document.getElementById("s1").innerText;
    //textareaを生成
    var area = document.createElement("textarea");
    //p要素の内容をtextareaに記述
    area.textContent = text;
    //生成したものをdocumentに追加
    document.body.appendChild(area);
    //選択/コピー
    area.select();
    document.execCommand("copy");
    //すぐに消す
    document.body.removeChild(area);
    alert('コピーしました！');
  }
  
